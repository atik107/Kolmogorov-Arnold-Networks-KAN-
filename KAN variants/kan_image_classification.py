    test_loader = DataLoader(test_dataset, batch_size=2048, shuffle=False)

    all_preds = []
    all_probs = []
    all_labels = []

    with torch.no_grad():
        for x, y in test_loader:
            logits = model(x)
            probs = torch.softmax(logits, dim=1)[:, 1]  # Probabilities of class 1
